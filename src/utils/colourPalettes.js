export function registerCornerstonePalettes(cornerstoneCore) {
  try {
    const { utilities } = cornerstoneCore;
    if (!utilities?.colormap?.registerColormap) {
      console.warn('[colourPalettes] registerColormap not available');
      return;
    }

    // CONFIRMED from CS3D v2.1.16 colormap.js source:
    //   registerColormap stores by colormap.Name (capital N)
    //   getColormap(name) retrieves by that same key
    //   setColormap calls cfun.applyColorMap(obj) which reads obj.RGBPoints
    //   RGBPoints: flat array [x, r, g, b, ...] with r/g/b in 0-1 range
    //   x is scalar value (remapped to data range by setMappingRange after)

    PALETTES.forEach(p => {
      const registryName = `petct_${p.id}`;
      const RGBPoints = [];
      for (let i = 0; i < 256; i++) {
        const [r, g, b] = getColor(p.id, i / 255);
        RGBPoints.push(i, r / 255, g / 255, b / 255);
      }
      utilities.colormap.registerColormap({
        Name:       registryName,
        ColorSpace: 'RGB',
        RGBPoints,
      });
    });

    console.log('[colourPalettes] Registered 13 palettes (Name+RGBPoints format)');
  } catch(e) {
    console.error('[colourPalettes] Registration error:', e);
  }
}
