/**
 * Register all 13 palettes into Cornerstone3D's colormap registry.
 * Call this once from cornerstone-init.js after CS3D is initialized.
 */
export function registerCornerstonePalettes(cornerstoneCore) {
  try {
    const { utilities } = cornerstoneCore;
    if (!utilities?.colormap?.registerColormap) {
      console.warn('[colourPalettes] registerColormap not available in this CS3D version');
      return;
    }
    PALETTES.forEach(p => {
      const colorData = Array.from({length: 256}, (_, i) => {
        const [r,g,b] = getColor(p.id, i/255);
        return { red: r, green: g, blue: b, alpha: 255 };
      });
      utilities.colormap.registerColormap({
        id:     `petct_${p.id}`,
        name:   p.label,
        colors: colorData,
      });
    });
    console.log('[colourPalettes] Registered 13 palettes into Cornerstone3D');
  } catch(e) {
    console.error('[colourPalettes] Registration error:', e);
  }
}
