/**
 * getColor(paletteId, t) → [r, g, b]
 * t ∈ [0, 1]
 */
export function getColor(id, t) {
  t = Math.max(0, Math.min(1, t));
  const i = Math.min(255, Math.floor(t * 255));

  switch(id) {
    case 'hot_iron':    return [HOT_IRON_R[i], HOT_IRON_G[i], HOT_IRON_B[i]];
    case 'pet':         return [PET_R[i], PET_G[i], PET_B[i]];
    case 'hot_metal_blue': return [HMB_R[i], HMB_G[i], HMB_B[i]];
    case 'pet_20_step': return [PET20.R[i], PET20.G[i], PET20.B[i]];
    case 'spring':      return [SPRING.R[i], SPRING.G[i], SPRING.B[i]];
    case 'summer':      return [SUMMER.R[i], SUMMER.G[i], SUMMER.B[i]];
    case 'fall':        return [FALL.R[i], FALL.G[i], FALL.B[i]];
    case 'winter':      return [WINTER.R[i], WINTER.G[i], WINTER.B[i]];
    case 'gray': {
      const v = Math.round(t * 255);
      return [v, v, v];
    }
    case 'inv_greyscale': {
      const v = Math.round((1 - t) * 255);
      return [v, v, v];
    }
    case 'inv_hot_iron': return [HOT_IRON_R[255-i], HOT_IRON_G[255-i], HOT_IRON_B[255-i]];
    case 'golden': {
      if(t < 0.5) return [Math.round(t*2*200), Math.round(t*2*140), 0];
      return [Math.round(200+(t-.5)*2*55), Math.round(140+(t-.5)*2*115), Math.round((t-.5)*2*200)];
    }
    case 'rainbow': return hslToRgb((1-t)*270/360, 1, 0.5);
    default: { const v=Math.round(t*255); return [v,v,v]; }
  }
}

/**
 * getCssGradient(paletteId) → CSS linear-gradient string (top→bottom, high→low)
 */
export function getCssGradient(id) {
  const stops = [0, 0.25, 0.5, 0.75, 1].map(t => {
    const [r,g,b] = getColor(id, t);
    return `rgb(${r},${g},${b}) ${t*100}%`;
  });
  return `linear-gradient(to bottom, ${stops.join(',')})`;
}

