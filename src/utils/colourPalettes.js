// PET 20-step (1.2.840.10008.1.5.4) — 20 solid colour blocks
function buildPet20Step() {
  const steps = [
    [0,0,0],[0,0,128],[0,0,255],[0,128,128],[0,255,0],
    [128,255,0],[255,255,0],[255,200,0],[255,164,0],[255,128,0],
    [255,80,0],[255,32,0],[255,0,0],[220,0,0],[180,0,0],
    [140,0,0],[100,0,60],[60,0,120],[120,60,200],[255,255,255]
  ];
  const R=[],G=[],B=[];
  for(let i=0;i<256;i++){
    const si=Math.min(19,Math.floor(i/12.8));
    R.push(steps[si][0]);G.push(steps[si][1]);B.push(steps[si][2]);
  }
  return{R,G,B};
}
